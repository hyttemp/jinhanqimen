// 【A6】Jtime — 儒略日轉日期時間（照抄 paipan.js）
// 回傳 [年, 月, 日, 時, 分, 秒]
// ════════════════════════════════════════════════════════════

Paipan.Jtime = function(jd) {
  jd = Paipan.floatval(jd);

  var y4h, init;
  if (jd >= 2299160.5) {
    y4h  = 146097;
    init = 1721119.5;
  } else {
    y4h  = 146100;
    init = 1721117.5;
  }

  var jdr = Math.floor(jd - init);
  var yh  = y4h / 4;
  var cen = Math.floor((jdr + 0.75) / yh);
  var d   = Math.floor(jdr + 0.75 - cen * yh);
  var ywl = 1461 / 4;
  var jy  = Math.floor((d + 0.75) / ywl);
  d = Math.floor(d + 0.75 - ywl * jy + 1);
  var ml  = 153 / 5;
  var mp  = Math.floor((d - 0.5) / ml);
  d = Math.floor((d - 0.5) - 30.6 * mp + 1);
  var y   = 100 * cen + jy;
  var m   = (mp + 2) % 12 + 1;
  if (m < 3) { y = y + 1; }

  var sd = Math.floor(
    (jd + 0.5 - Math.floor(jd + 0.5)) * 24 * 60 * 60 + 0.00005
  );
  var mt2 = Math.floor(sd / 60);
  var sc  = sd % 60;
  var hh  = Math.floor(mt2 / 60);
  var mn  = mt2 % 60;

  return [Math.floor(y), Math.floor(m), Math.floor(d), hh, mn, sc];
};

