// 【A5】Jdays — 日期轉儒略日（照抄 paipan.js）
// ════════════════════════════════════════════════════════════

Paipan.Jdays = function(yy, mm, dd, hh, mt, ss) {
  yy = Paipan.floatval(yy);
  mm = Paipan.floatval(mm);
  dd = Paipan.floatval(dd);
  hh = (hh === undefined) ? 12 : Paipan.floatval(hh);
  mt = (mt === undefined) ?  0 : Paipan.floatval(mt);
  ss = (ss === undefined) ?  0 : Paipan.floatval(ss);

  var yp = yy + Math.floor((mm - 3) / 10);

  var init, jdy;
  if ((yy > 1582) ||
      (yy === 1582 && mm > 10) ||
      (yy === 1582 && mm === 10 && dd >= 15)) {
    init = 1721119.5;
    jdy  = Math.floor(yp * 365.25)
         - Math.floor(yp / 100)
         + Math.floor(yp / 400);
  } else if ((yy < 1582) ||
             (yy === 1582 && mm < 10) ||
             (yy === 1582 && mm === 10 && dd <= 4)) {
    init = 1721117.5;
    jdy  = Math.floor(yp * 365.25);
  } else {
    return false; // 1582-10-05 ~ 1582-10-14 不存在
  }

  var mp  = Math.floor(mm + 9) % 12;
  var jdm = mp * 30 + Math.floor((mp + 1) * 34 / 57);
  var jdd = dd - 1;
  hh = hh + ((ss / 60) + mt) / 60;
  var jdh = hh / 24;

  return jdy + jdm + jdd + jdh + init;
};

