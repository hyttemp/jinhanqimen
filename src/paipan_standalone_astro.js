// 使用克卜勒橢圓軌道計算，考慮地球公轉不均勻性
// 索引 0-23（從春分起）
// 回傳力學時 TT（不含攝動、不含 DeltaT、不含時區）
// ════════════════════════════════════════════════════════════

Paipan.MeanJQJD = function(yy) {
  yy = Paipan.intval(yy);

  var jdez = [];
  var jdve = Paipan.VE(yy);
  var ty   = Paipan.VE(yy + 1) - jdve; // 本年回歸年長

  var ath = 2 * Math.PI / 24;
  var tx  = (jdve - 2451545) / 365250;

  // 地球軌道離心率
  var e = 0.0167086342
        - 0.0004203654 * tx
        - 0.0000126734 * tx*tx
        + 0.0000001444 * tx*tx*tx
        - 0.0000000002 * tx*tx*tx*tx
        + 0.0000000003 * tx*tx*tx*tx*tx;

  // 近日點黃經（以西元年/1000 為時間變數，照抄 paipan.js 原版）
  var tt  = yy / 1000;
  var vp  = 111.25586939
          -  17.0119934518333 * tt
          -   0.044091890166673 * tt*tt
          -   4.37356166661345e-4 * tt*tt*tt
          +   8.16716666602386e-6 * tt*tt*tt*tt;
  var rvp = vp * 2 * Math.PI / 360;

  var peri = [];
  for (var i = 1; i <= 24; i++) {
    var flag = 0;
    var th   = ath * (i - 1) + rvp;

    if (th > Math.PI && th <= 3 * Math.PI) {
      th   = 2 * Math.PI - th;
      flag = 1;
    }
    if (th > 3 * Math.PI) {
      th   = 4 * Math.PI - th;
      flag = 2;
    }

    var f1 = 2 * Math.atan(
      Math.sqrt((1 - e) / (1 + e)) * Math.tan(th / 2)
    );
    var f2 = (e * Math.sqrt(1 - e*e) * Math.sin(th))
           / (1 + e * Math.cos(th));
    var f  = (f1 - f2) * ty / 2 / Math.PI;

    if (flag === 1) { f = ty - f; }
    if (flag === 2) { f = 2 * ty - f; }

    peri[i] = f;
  }

  for (var i = 1; i <= 24; i++) {
    jdez[i - 1] = jdve + peri[i] - peri[1];
  }
  return jdez;
};

