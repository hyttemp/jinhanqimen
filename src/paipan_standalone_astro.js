// 【A11】GetAdjustedJQ — 24節氣精確儒略日（照抄 paipan.js）
//
// 索引 0-23（從春分起）：
//   0=春分  1=清明  2=穀雨  3=立夏  4=小滿  5=芒種
//   6=夏至  7=小暑  8=大暑  9=立秋 10=處暑 11=白露
//  12=秋分 13=寒露 14=霜降 15=立冬 16=小雪 17=大雪
//  18=冬至 19=小寒 20=大寒 21=立春 22=雨水 23=驚蟄
//
// 計算流程（照抄 paipan.js GetAdjustedJQ）：
//   MeanJQJD(yy)        → 各節氣平均 JDE（TT，克卜勒）
//   + Perturbation()    → 行星攝動修正（TT）
//   - DeltaT/60/24      → TT 轉 UT（分鐘 → 天）
//   + 8/24              → UT 轉 CST（UTC+8）
//   + jqXFu[]           → 歷史修正（精度為日）
//
// 回傳值已含北京時間 +8h
// ════════════════════════════════════════════════════════════

Paipan.JQ = {};

Paipan.GetAdjustedJQ = function(yy, calendar) {
  yy       = Paipan.intval(yy);
  calendar = calendar ? 1 : 0;

  if (Paipan.JQ[yy] === undefined) {
    Paipan.JQ[yy] = [];
  }

  if (Paipan.JQ[yy][0] === undefined) {
    var jdjq = [];
    var jdez = Paipan.MeanJQJD(yy); // TT，克卜勒

    for (var i = 0; i < 24; i++) {
      var ptb = Paipan.Perturbation(jdez[i]);               // 攝動修正（天）
      var dt  = Paipan.DeltaT(yy, Math.ceil(i / 2) + 3);   // 分鐘
      jdjq[i] = jdez[i] + ptb - dt / 60 / 24;              // TT → UT
      jdjq[i] = jdjq[i] + 8 / 24;                          // UT → CST
    }

    Paipan.JQ[yy][0] = jdjq;
  }

  // 若只需現代精確值（calendar=false），直接回傳
  if (Paipan.JQ[yy][calendar]) {
    return Paipan.JQ[yy][calendar].slice(0);
  }

  var refer = true;
  var jdjq  = Paipan.JQ[yy][0].slice(0);

  if (calendar === 1) {
    // 古代農曆：qiKB 線性擬合
    var B  = Paipan.qiKB;
    var pc = 7;
    var f1 = B[0] - pc;
    var f2 = B[B.length - 1] - pc;

    if (((jdjq[0] >= f1) && (jdjq[0] < f2)) ||
        ((jdjq[23] >= f1) && (jdjq[23] < f2))) {
      refer = false;
      for (var i = 0, j = 0; j < 24; j++) {
        if ((jdjq[j] < f1) || (jdjq[j] >= f2)) { continue; }
        if (jdjq[j] + pc >= B[i + 2]) {
          for (; i < B.length; i += 2) {
            if (jdjq[j] + pc < B[i + 2]) { break; }
          }
        }
        jdjq[j] = B[i] + B[i + 1] * Math.floor(
          (jdjq[j] + pc - B[i]) / B[i + 1]
        );
        jdjq[j] = Math.floor(jdjq[j] + 0.5);
        if (jdjq[j] === 1683460) { jdjq[j]++; }
      }
    }

    // jqXFu 時宪曆修正（物件格式，照抄 paipan.js）
    var xf = Paipan.jqXFu[yy];
    if (xf) {
      refer = false;
      for (var idx in xf) {
        jdjq[idx] += xf[idx];
        jdjq[idx]  = Math.floor(jdjq[idx] + 0.5);
      }
    }
  }

  Paipan.JQ[yy][calendar] = refer ? Paipan.JQ[yy][0] : jdjq;
  return jdjq;
};

